DROP PROCEDURE IF EXISTS PROCEDURE_CHECK_PERMISSION_USUER;

DELIMITER //
	CREATE PROCEDURE PROCEDURE_CHECK_PERMISSION_USUER(IN INPUT_USER_ID INT)
		BEGIN
		    SELECT users.is_admin FROM users WHERE users.is_active = 'ACTIVE' AND users.id = INPUT_USER_ID;
		END //
DELIMITER ;

CALL PROCEDURE_CHECK_PERMISSION_USUER(1)